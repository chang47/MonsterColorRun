package metaModel;

public class RouteMonsters {
	int id;
	int monsterId;
	int routeId;
	int capture;
	int level;
	
	public RouteMonsters(int id, int monsterId, int routeId, int capture,
			int level) {
		this.id = id;
		this.monsterId = monsterId;
		this.routeId = routeId;
		this.capture = capture;
		this.level = level;
	}
	
}
